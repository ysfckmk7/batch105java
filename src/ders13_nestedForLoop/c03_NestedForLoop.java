package ders13_nestedForLoop;

public class c03_NestedForLoop {
    public static void main(String[] args) {
        // 1-4 arasınddakı sayılar icin carPım tablosu olusturalım

        // dıs dongu satırları kontrol eder
        for (int i =1; i <=4 ; i++) {

            // ic dongu her sutundakı olan degerlerı kontrol eder
            for (int j =1; j <=4 ; j++) {
                System.out.print(i*j + " ");
            }
            // ic dongu bıttıgınde javayı alt satıra indirmeliyiz
            System.out.println("");







        }









    }
}
